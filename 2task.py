# Дані про товари
products = [
    ("Хліб", 25.50, 2),
    ("Молоко", 33.75, 1),
    ("Яблука", 12.30, 10)
]

# Виводимо заголовки
print("{:<20} {:>10} {:^8}".format("Назва товару", "Ціна", "Кільк."))

# Виводимо кожен рядок таблиці
for name, price, quantity in products:
    print("{:<20} {:>10.2f} {:^8}".format(name, price, quantity))
